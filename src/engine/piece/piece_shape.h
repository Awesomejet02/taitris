static inline
const PieceShape *piece_shape(PieceType type) {
  switch (type) {
    case PIECE_TYPE_I:
      return &PIECE_SHAPE_I;
    case PIECE_TYPE_O:
      return &PIECE_SHAPE_O;
    case PIECE_TYPE_T:
      return &PIECE_SHAPE_T;
    case PIECE_TYPE_L:
      return &PIECE_SHAPE_L;
    case PIECE_TYPE_J:
      return &PIECE_SHAPE_J;
    case PIECE_TYPE_Z:
      return &PIECE_SHAPE_Z;
    case PIECE_TYPE_S:
      return &PIECE_SHAPE_S;
    default:
      errx(EXIT_FAILURE, "Unknown piece type %d", type);
  }
}

